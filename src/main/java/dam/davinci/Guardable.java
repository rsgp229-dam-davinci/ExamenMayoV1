package dam.davinci;

/**
 * Interfaz funcional que define el comportamiento 'guardable' de
 * cualquier objeto que lo implemente.
 *
 * @author Rafael
 */
public interface Guardable {
    /**
     * <p>Este método debe permitir guardar cualquier información que se
     * determine en su llamada.</p>
     */
    public void guardar();
}
